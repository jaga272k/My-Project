<div class="para_1">
<p id="para1">The Ministry of Road Transport & Highways (MoRTH) has been facilitating
 computerization of over 1300+ Road Transport Offices (RTOs) across the country.
 RTOs issue Registration Certificate (R.C.) & Driving License (D.L.) that are 
 mandatory requirements and are valid across the country, subject to certain 
 provisions and permissions.</p>
 <p id="para2">With wide variations in state policies and manual/ system based procedures
 being followed across the country, it had become necessary to define same 
 standards for these documents on a pan-India level to ensure interoperability,
 correctness and timely availability of information. SCOSTA committee setup 
 for this purpose had recommended a uniform standardized software across the
 country.</p>
</div>
<div class=" para_2">
<h1>OUR PURPOSE</h1>
<p>To improve the quality of service delivery to the citizen.</p>
</div>
<div class="para_3">
<h1>OUR MISSION</h1>
<p>To automate all Vehicle Registration and Driving License related activities in 
transport authorities of country with introduction of smart card technology to 
handle issues like inter state transport vehicle movement and to create state 
and national level registers of vehicles/DL information</p>
</div>
<div class="para_4">
<h1>OUR OBJECTIVES<h1>
<ul class="ul">
<li>Better services to Transport Department as well as citizen</li>
<li>Quick implementation of government policies from time to time</li>
<li>Improved image of Government & Department</li>
<li>Instant access of Vehicle/DL information to other government departments</li>
</ul>
